print(f'True or True: {True or True}')
print(f'True or False: {True or False}')
print(f'False or True: {False or True}')
print(f'False or False: {False or False}')
print(f'0 or "hello": {0 or "hello"}')
print(f'bool(0 or "hello"): {bool(0 or "hello")}')
print(f'"hello" or 123: {"hello" or 123}')
print(f'bool("hello" or 123): {bool("hello" or 123)}')
print(f'"" or 2023: {"" or 2023}')
print(f'bool("" or 2023): {bool("" or 2023)}')